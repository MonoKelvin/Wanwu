/** 将段落描述转为 Markdown：段首「标签：」加粗，段间空行保留 */
function desc(text) {
  return text
    .split('\n\n')
    .map((block, index) => {
      const trimmed = block.trim()
      const m = trimmed.match(/^([^：\n]{2,16})：([\s\S]*)$/)
      if (m && index > 0) return `**${m[1]}**：${m[2]}`
      return trimmed
    })
    .join('\n\n')
}

function item(
  slug,
  categoryId,
  subCategoryId,
  name,
  summary,
    description,
    tags,
    specs
  ) {
  const base = `library/${categoryId}/${slug}`
  return {
    slug,
    categoryId,
    subCategoryId,
    name,
    summary,
    description: desc(description),
    tags,
    specs,
    coverFile: `${base}/cover.jpg`,
    galleryFiles: [
      `${base}/gallery-01.jpg`,
      `${base}/gallery-02.jpg`,
      `${base}/gallery-03.jpg`
    ],
    mediaProvider: 'pixabay'
  }
}

